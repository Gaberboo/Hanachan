use std::f32::consts::PI;

pub trait F32Ext {
    fn wii_sqrt(self) -> Self;
    fn wii_sin(self) -> Self;
    fn wii_sin_inner(self) -> Self;
    fn wii_cos(self) -> Self;
    fn wii_cos_inner(self) -> Self;
    fn wii_atan2(self, other: Self) -> Self;
}

impl F32Ext for f32 {
    fn wii_sqrt(self) -> f32 {
        fn ppc_frsqrte(val: f64) -> f64 {
            let bits = val.to_bits();
            let mut mantissa = bits & ((1 << 52) - 1);
            let sign = bits & 1 << 63;
            let mut exponent = bits & 0x7ff << 52;

            // Special case 0
            if mantissa == 0 && exponent == 0 {
                return val.signum() * f64::MAX;
            }

            // Special case NaN-ish numbers
            if exponent == 0x7ff << 52 {
                if mantissa == 0 {
                    if sign != 0 {
                        return f64::NAN;
                    }
                    return 0.0;
                }
                return 0.0 + val;
            }

            // Negative numbers return NaN
            if sign != 0 {
                return f64::NAN;
            }

            if exponent == 0 {
                // "Normalize" denormal values
                while mantissa & 1 << 52 == 0 {
                    exponent -= 1 << 52;
                    mantissa <<= 1;
                }
                mantissa &= (1 << 52) - 1;
                exponent += 1 << 52;
            }

            let odd_exponent = exponent & 1 << 52 == 0;
            exponent = ((0x3ff << 52) - ((exponent - (0x3fe << 52)) / 2)) & (0x7ff << 52);
            let mut bits = sign | exponent;

            let i = mantissa >> 37;
            let mut idx = (i / 2048) as usize;
            if odd_exponent {
                idx += 16;
            }
            bits |= (FRSQRTE_BASES[idx] - FRSQRTE_DECS[idx] * (i % 2048)) << 26;

            f64::from_bits(bits)
        }

        fn f64_25_bit_mantissa(val: f64) -> f64 {
            let bits = val.to_bits();
            let bits = (bits & 0xfffffffff8000000) + (bits & 0x8000000);
            f64::from_bits(bits)
        }

        if self <= 0.0 {
            0.0
        } else {
            let recip_sqrt = ppc_frsqrte(self as f64);
            let tmp0 = (recip_sqrt * f64_25_bit_mantissa(recip_sqrt)) as f32;
            let tmp1 = (recip_sqrt * 0.5) as f32;
            let tmp2 = (3.0 - tmp0 as f64 * self as f64) as f32;
            tmp1 * tmp2 * self
        }
    }

    fn wii_sin(self) -> f32 {
        let step = 256.0 / (2.0 * PI);
        (self * step).wii_sin_inner()
    }

    fn wii_sin_inner(self) -> f32 {
        let mut f_idx = self.abs();
        while f_idx > 65536.0 {
            f_idx -= 65536.0;
        }
        let idx = f_idx as usize % 256;
        self.signum() * (TRIG_TABLE[idx][0] + f_idx.fract() * TRIG_TABLE[idx][2])
    }

    fn wii_cos(self) -> f32 {
        let step = 256.0 / (2.0 * PI);
        (self * step).wii_cos_inner()
    }

    fn wii_cos_inner(self) -> f32 {
        let mut f_idx = self.abs();
        while f_idx > 65536.0 {
            f_idx -= 65536.0;
        }
        let idx = f_idx as usize % 256;
        TRIG_TABLE[idx][1] + f_idx.fract() * TRIG_TABLE[idx][3]
    }

    fn wii_atan2(self, other: f32) -> f32 {
        let y = self;
        let x = other;

        if x == 0.0 && y == 0.0 {
            return 0.0;
        }

        let (a, b, c, d) = if x < 0.0 {
            if y < 0.0 {
                if -x < -y {
                    (-y, -x, -64.0, -1.0)
                } else {
                    (-x, -y, -128.0, 1.0)
                }
            } else {
                if -x < y {
                    (y, -x, 64.0, 1.0)
                } else {
                    (-x, y, 128.0, -1.0)
                }
            }
        } else {
            if y < 0.0 {
                if x < -y {
                    (-y, x, -64.0, 1.0)
                } else {
                    (x, -y, 0.0, -1.0)
                }
            } else {
                if x < y {
                    (y, x, 64.0, -1.0)
                } else {
                    (x, y, 0.0, 1.0)
                }
            }
        };

        let f_idx = b / a * 32.0;
        let idx = f_idx as usize % 32;
        0.024543693 * (c + d * (ATAN2_BASES[idx] + f_idx.fract() * ATAN2_INCS[idx]))
    }
}

